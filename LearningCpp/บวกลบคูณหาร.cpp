#include <iostream>
using namespace std;
int main() {
    int a = 11, b = 5;
    cout << "a = " << a << ",b = " << b << endl;
    cout << "a + b = " << a+b << endl; //การคูณ
    cout << "a - b = " << a-b << endl; //การลบ
    cout << "a * b = " << a*b << endl; //การคูณ
    cout << "a / b = " << a/b << endl; //การหาร
    cout << "a % b = " << a%b << endl; //การหารเอาเศษ
}