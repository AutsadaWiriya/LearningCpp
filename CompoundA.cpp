#include <iostream>
using namespace std;
int main() {
    int a = 100;
    a+=50 ; //a = a+50
    cout << "a = " << a << endl;

    int b = 100;
    b-=50 ; //b = b+50
    cout << "b = " << b << endl;

    int c = 100;
    c*=50 ; //c = c+50
    cout << "c = " << c << endl;

    int d = 100;
    d/=50 ; //d = d+50
    cout << "d = " << d << endl;

    int e = 100;
    e%=50 ; //e = e+50
    cout << "e = " << e << endl;
}